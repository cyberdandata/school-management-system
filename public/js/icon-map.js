// ==================== ICON HELPER FUNCTIONS ====================
function getIcon(iconClass) {
    // Extract the main icon class name (e.g., 'fa-users' from 'fas fa-users')
    const classes = iconClass.split(' ');
    let iconKey = null;
    
    for (const cls of classes) {
        if (cls.startsWith('fa-')) {
            iconKey = cls;
            break;
        }
    }
    
    if (!iconKey) return '❓';
    return IconMap[iconKey] || '❓';
}

function renderIcon(iconClass, size = '1em') {
    const emoji = getIcon(iconClass);
    return `<span class="icon" style="font-size: ${size}">${emoji}</span>`;
}

function renderIconWithText(iconClass, text, size = '1em') {
    return `${renderIcon(iconClass, size)} ${text}`;
}

